#define AnimateCount 4
int AnimateIndex=0;
int AnimatePos[AnimateCount][2]={{10,0},{6,10},{4, 20},{3, 27}};

int ShowFPAnimate(int x, int y)
{
	int bot=y+AnimatePos[AnimateCount-1][1]+22;
	AnimateIndex++;
	if(AnimateIndex>=AnimateCount) AnimateIndex=0;
	if(gLangDriver && gLangDriver->RightToLeft)
	{
		LCD_ClearBar(gLCDWidth-x-50,y,gLCDWidth-x,bot);
		LCD_OutBMP1Bit(gLCDWidth-(x+AnimatePos[AnimateIndex][0])-0x30, y+AnimatePos[AnimateIndex][1],
			Icon_Finger, 0, 0, 0x30, 0x16, 0);
		LCD_Bar(gLCDWidth-x-10,bot-4,gLCDWidth-x, bot);
	}
	else
	{
		LCD_ClearBar(x,y,x+50,bot);
		LCD_OutBMP1Bit(x+AnimatePos[AnimateIndex][0], y+AnimatePos[AnimateIndex][1],
			Icon_Finger, 0, 0, -1, -1, 0);
		LCD_Bar(x,bot-4,x+10, bot);
	}
	LCDInvalid();
	return 1;
}

